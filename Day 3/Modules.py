# 1. Write a Python program to use the math module to calculate the square root of a number.
import math

num = 25
sqrt_num = math.sqrt(num)
print("Square root of", num, "is", sqrt_num)

# 2. Write a Python program to generate a random number between 1 and 100 using the random module.
