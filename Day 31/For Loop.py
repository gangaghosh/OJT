# for loop is used when we want to iterate over a sequence

# Example 
# For loop with list
list=['C','C++','Java','C#'] 
print("This is our list ",list) 
i=1 
for val in list: 
    print("List Item",i,"is",val) 
    i=i+1
    
# For loop with tuple
tuple=('C','C++','Java','C#','Python') 
print("This is our Tuple ",tuple)
i=1    
for val in tuple:
    print("Tuple Item",i,"is",val)
    i=i+1

# For loop with string
string="C-Sharp Corner" 
for val in string :
    print(val)
    

